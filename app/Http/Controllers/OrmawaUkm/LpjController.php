<?php

namespace App\Http\Controllers\OrmawaUkm;

use App\Http\Controllers\Controller;
use App\Models\Lpj;
use Illuminate\Http\Request;
use Illuminate\Support\Facades\Auth;

class LpjController extends Controller
{
    /*
    |--------------------------------------------------------------------------
    | INDEX
    |--------------------------------------------------------------------------
    */

    public function index()
    {
        /*
        |--------------------------------------------------------------------------
        | USER LOGIN
        |--------------------------------------------------------------------------
        */

        $user = Auth::user();

        /*
        |--------------------------------------------------------------------------
        | DATA LPJ
        |--------------------------------------------------------------------------
        */

        $lpjs = Lpj::where(
                    'user_id',
                    $user->id
                )
                ->latest()
                ->get();

        /*
        |--------------------------------------------------------------------------
        | STATISTIK
        |--------------------------------------------------------------------------
        */

        $totalLpj = $lpjs->count();

        $disetujui = $lpjs->where(
            'status',
            'disetujui'
        )->count();

        $revisi = $lpjs->whereIn(
            'status',
            ['revisi', 'ditolak']
        )->count();

        $menunggu = $lpjs->where(
            'status',
            'pending'
        )->count();

        /*
        |--------------------------------------------------------------------------
        | VIEW
        |--------------------------------------------------------------------------
        */

        return view(
            'ormawaukm.lpj.index',
            compact(
                'lpjs',
                'totalLpj',
                'disetujui',
                'revisi',
                'menunggu'
            )
        );
    }

    /*
    |--------------------------------------------------------------------------
    | CREATE
    |--------------------------------------------------------------------------
    */

    public function create()
    {
        return view('ormawaukm.lpj.create');
    }

    /*
    |--------------------------------------------------------------------------
    | STORE
    |--------------------------------------------------------------------------
    */

    public function store(Request $request)
{
    /*
    |--------------------------------------------------------------------------
    | VALIDASI
    |--------------------------------------------------------------------------
    */

    $request->validate([

        'judul' => 'required',

        'deskripsi' => 'required',

        'file_lpj' => 'required|file|mimes:pdf,doc,docx|max:2048',

    ]);

    /*
    |--------------------------------------------------------------------------
    | AMBIL FILE
    |--------------------------------------------------------------------------
    */

    $file = $request->file('file_lpj');

    /*
    |--------------------------------------------------------------------------
    | BUAT NAMA FILE
    |--------------------------------------------------------------------------
    */

    $namaFile = time() . '_' . $file->getClientOriginalName();

    /*
    |--------------------------------------------------------------------------
    | PINDAHKAN FILE
    |--------------------------------------------------------------------------
    */

    $file->move(

        public_path('uploads/lpj'),

        $namaFile

    );

    /*
    |--------------------------------------------------------------------------
    | SIMPAN DATABASE
    |--------------------------------------------------------------------------
    */

    Lpj::create([

        'user_id' => Auth::id(),

        'judul' => $request->judul,

        'deskripsi' => $request->deskripsi,

        /*
        |--------------------------------------------------------------------------
        | KOLOM FILE
        |--------------------------------------------------------------------------
        */

        'file' => $namaFile,

        'status' => 'pending',

    ]);

    /*
    |--------------------------------------------------------------------------
    | REDIRECT
    |--------------------------------------------------------------------------
    */

    return redirect()
        ->route('ormawaukm.lpj.index')
        ->with(
            'success',
            'LPJ berhasil diupload'
        );
}
}