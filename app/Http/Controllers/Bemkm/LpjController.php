<?php

namespace App\Http\Controllers\Bemkm;

use App\Http\Controllers\Controller;
use App\Models\Lpj;
use Illuminate\Http\Request;

class LpjController extends Controller
{
    /*
    |--------------------------------------------------------------------------
    | INDEX
    |--------------------------------------------------------------------------
    */

    public function index()
    {
        /*
        |--------------------------------------------------------------------------
        | DATA LPJ
        |--------------------------------------------------------------------------
        */

        $lpjs = Lpj::latest()->get();

        /*
        |--------------------------------------------------------------------------
        | STATISTIK
        |--------------------------------------------------------------------------
        */

        $totalLpj = Lpj::count();

        $pendingLpj = Lpj::where(
            'status',
            'pending'
        )->count();

        $approvedLpj = Lpj::where(
            'status',
            'disetujui'
        )->count();

        $rejectedLpj = Lpj::whereIn(
            'status',
            ['revisi', 'ditolak']
        )->count();

        /*
        |--------------------------------------------------------------------------
        | VIEW
        |--------------------------------------------------------------------------
        */

        return view(
            'bemkm.lpj.index',
            compact(
                'lpjs',
                'totalLpj',
                'pendingLpj',
                'approvedLpj',
                'rejectedLpj'
            )
        );
    }

    /*
    |--------------------------------------------------------------------------
    | UPDATE STATUS
    |--------------------------------------------------------------------------
    */

    public function updateStatus(Request $request, $id)
    {
        /*
        |--------------------------------------------------------------------------
        | VALIDASI STATUS
        |--------------------------------------------------------------------------
        */

        $request->validate([

            'status' => 'required'

        ]);

        /*
        |--------------------------------------------------------------------------
        | CARI LPJ
        |--------------------------------------------------------------------------
        */

        $lpj = Lpj::findOrFail($id);

        /*
        |--------------------------------------------------------------------------
        | UPDATE STATUS
        |--------------------------------------------------------------------------
        */

        $lpj->update([

            'status' => $request->status

        ]);

        /*
        |--------------------------------------------------------------------------
        | REDIRECT
        |--------------------------------------------------------------------------
        */

        return back()->with(
            'success',
            'Status LPJ berhasil diperbarui'
        );
    }
}