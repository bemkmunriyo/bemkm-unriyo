<?php

namespace App\Http\Controllers;

use App\Models\Berita;

class BeritaPublicController extends Controller
{
    /*
    |--------------------------------------------------------------------------
    | HALAMAN BERITA PUBLIK
    |--------------------------------------------------------------------------
    */

    public function index()
    {
        /*
        |--------------------------------------------------------------------------
        | AMBIL BERITA PUBLISHED
        |--------------------------------------------------------------------------
        */

        $beritas = Berita::where(
            'status',
            'published'
        )
        ->latest()
        ->get();

        /*
        |--------------------------------------------------------------------------
        | AMBIL PENGUMUMAN
        |--------------------------------------------------------------------------
        */

        $pengumuman = Berita::where(
            'kategori',
            'Pengumuman'
        )
        ->where(
            'status',
            'published'
        )
        ->latest()
        ->take(3)
        ->get();

        /*
        |--------------------------------------------------------------------------
        | VIEW
        |--------------------------------------------------------------------------
        */

        return view(
            'berita.index',
            compact(
                'beritas',
                'pengumuman'
            )
        );
    }

    /*
    |--------------------------------------------------------------------------
    | DETAIL BERITA
    |--------------------------------------------------------------------------
    */

    public function show($slug)
    {
        /*
        |--------------------------------------------------------------------------
        | CARI BERITA BERDASARKAN SLUG
        |--------------------------------------------------------------------------
        */

        $berita = Berita::where(
            'slug',
            $slug
        )
        ->where(
            'status',
            'published'
        )
        ->firstOrFail();

        /*
        |--------------------------------------------------------------------------
        | VIEW
        |--------------------------------------------------------------------------
        */

        return view(
            'berita.detail',
            compact('berita')
        );
    }
}